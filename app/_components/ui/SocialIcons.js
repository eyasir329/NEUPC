/**
 * Map of platform name → icon component for dynamic rendering.
 * @type {Record<string, React.ComponentType<{ className?: string }>>}
 */
export const SOCIAL_ICON_MAP = {
  github: GitHubIcon,
  linkedin: LinkedInIcon,
  facebook: FacebookIcon,
  twitter: TwitterIcon,
  youtube: YouTubeIcon,
  website: GlobeIcon,
};

/**
 * Render a social icon by platform name.
 * @param {{ platform: string, className?: string }} props
 */
export function SocialIcon({ platform, className = 'h-5 w-5' }) {
  const Icon = SOCIAL_ICON_MAP[platform?.toLowerCase()] || GitHubIcon;
  return <Icon className={className} />;
}
